/**
 * Pure formatting utilities with no server/database dependencies.
 * Use this from client components to avoid pulling supabase-server into the browser bundle.
 */
export function formatNumber(num: number): string {
  if (num >= 1000000) {
    return (num / 1000000).toFixed(1) + 'M'
  }
  if (num >= 1000) {
    return (num / 1000).toFixed(1) + 'K'
  }
  return num.toString()
}
